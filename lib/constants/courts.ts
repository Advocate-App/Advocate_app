export function getCourtColor(code: string): string {
  if (code.startsWith('MACT')) return '#dbeafe'
  if (code.startsWith('DJ') || code.startsWith('DC') || code.startsWith('ADJ') || code.startsWith('CJM') || code.startsWith('ACJM')) return '#f3f4f6'
  if (code.startsWith('NI')) return '#fef9c3'
  if (code.startsWith('DCF') || code.startsWith('CONS') || code.startsWith('STATE')) return '#dcfce7'
  if (code === 'jodhpur' || code === 'jaipur') return '#ede9fe'
  if (code.startsWith('PLA') || code.startsWith('LC') || code.startsWith('WC')) return '#fff7ed'
  return '#f9fafb'
}

